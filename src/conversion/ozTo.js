/* Oz. to X */
export function ozToLiter(oz) {
  return oz / 33.814;
}

export function ozToGal(oz) {
  return oz / 128;
}

/* Liter to X */
export function literToOz(l) {
  return l * 33.814;
}

export function literToGal(l) {
  return l * 0.26417;
}

export function literToCl(l) {
  return l * 100;
}
